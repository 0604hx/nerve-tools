public class PageResult {
    private long total;
    private List rows;
	private List footer;

    public PageResult(){
    }

    public PageResult(long t, List rows){
        this.total = t;
        this.rows = rows;
    }

	public PageResult(long t, List rows, List footer){
		this(t, rows);
		this.footer = footer;
	}

    public long getTotal() {
        return total;
    }

    public void setTotal(long total) {
        this.total = total;
    }

    public List getRows() {
        return rows;
    }

    public void setRows(List rows) {
        this.rows = rows;
    }
	public List getFooter() {
		return footer;
	}

	public void setFooter(List footer) {
		this.footer = footer;
	}
}
